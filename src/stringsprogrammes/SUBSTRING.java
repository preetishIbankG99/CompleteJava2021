package stringsprogrammes;

public class SUBSTRING {
    public static void main(String[] args) {
        String s = "javatpoint tutorial";
        System.out.println(s.substring(2,4));//va
        System.out.println(s.substring(2));//vatpoint
        System.out.println(s.substring(10));//tutorial
        System.out.println(s.substring(0));//javatpoint tutorial
    }
}
